#include <stdio.h>

int main(int argc, char const *argv[])
{
    int n[] = {5, 4, 3, 2, 1};

    // 시작 인덱스: 1부터 시작해서 5번 반복하면서 % 5로 순환
    for (int i = 0; i < 5; i++) {
        printf("%d", n[(i + 1) % 5]);
        // i=0 → (0+1)%5 = 1 → n[1] = 4
        // i=1 → (1+1)%5 = 2 → n[2] = 3
        // i=2 → (2+1)%5 = 3 → n[3] = 2
        // i=3 → (3+1)%5 = 4 → n[4] = 1
        // i=4 → (4+1)%5 = 0 → n[0] = 5
    }

    return 0;
}